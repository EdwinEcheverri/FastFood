-- CONSULTAS ADICIONALES A REALIZAR SOBRE LA BASE DE DATOS

-- Clientes frecuentes (Clientes con más de 1 órden)
SELECT 
    c.ClienteID,
    c.Nombre,
    COUNT(o.OrdenID) AS TotalOrdenes
FROM Clientes c
INNER JOIN Ordenes o ON c.ClienteID = o.ClienteID
GROUP BY c.ClienteID, c.Nombre
HAVING COUNT(o.OrdenID) > 1
ORDER BY TotalOrdenes DESC;

-- Total de kilometros recorridos por cada mensajero
SELECT 
    MensajeroID,
    SUM(KilometrosRecorrer) AS TotalKilometros
FROM Ordenes
GROUP BY MensajeroID
ORDER BY TotalKilometros DESC;

-- Cantidad de empleados por departamento
SELECT 
    Departamento,
    SucursalID,
    COUNT(*) AS TotalEmpleados
FROM Empleados
GROUP BY Departamento, SucursalID
ORDER BY Departamento, SucursalID;

--Tipos de Pago Más Utilizados
SELECT 
    TipoPagoID,
    COUNT(*) AS CantidadUsos
FROM Ordenes
GROUP BY TipoPagoID
ORDER BY CantidadUsos DESC;

--Ingresos generados por cada producto
SELECT 
    p.Nombre,
    SUM(do.Cantidad * do.Precio) AS TotalGenerado
FROM DetalleOrdenes do
JOIN Productos p ON do.ProductoID = p.ProductoID
GROUP BY p.Nombre
ORDER BY TotalGenerado DESC;
